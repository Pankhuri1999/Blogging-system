Writing can also unlock some of the important thoughts you might otherwise dismiss or lose. Ordinarily, our minds are buzzing with random thoughts and ideas, but we tend to let them go easily because we view them as unimportant. When writing, you’re forced to evaluate your ideas with deliberate attentiveness, sorting through them in an attempt to find the perfect ones for your piece. This is also beneficial when you’re on the lookout for new topics and generating possible new angles for development; the creative process here can help you improve your creative processes in other areas of your profession, such as coming up with innovative solutions to pressing problems.</div>

